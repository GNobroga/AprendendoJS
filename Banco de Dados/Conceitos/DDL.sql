`

    DDL - Data Definition Language

        COMANDOS: Create, Alter, Drop

        Este subgrupo da linguagem SQL é utilizado para criar, alterar e 
        excluir bancos de dados, tabelas, e elementos associados como índice
        e visão.


        CREATE - Usado para criar um banco de dados, tabela e outros objetos em um banco de dados.
        ALTER - Usado para alterar a estrutura de tabelas ou outro objeto em um banco de dados.
        DROP - Usado para apagar bancos de dados, tabelas e outros objetos.

        EXEMPLOS:

            CREATE DATABASE nome;
            CREATE TABLE nome (atributo1 INT PRIMARY KEY, atributo2 VARCHAR(50));

            ALTER TABLE tabela ADD atributo DECIMAL(8,2);

            DROP TABLE tabela;
            DROP DATABASE nome;


`;
