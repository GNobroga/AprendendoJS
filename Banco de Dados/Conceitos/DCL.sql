`
    DCL - Data Control Language

        Este subgrupo da linguagem SQL é utilizado para controlar os aspectos de autorização
        de dados e licenças de usuários para controlar quem tem acesso para 
        manipular dados dentro do banco de dados.

        COMANDOS: Grant e Revoke

        GRANT - Usado para autorizar um usuário a executar ou setar operações no banco de dados.
        REVOKE - Usado para remover ou restringir a capacidade de um usuário de executar operações.

        GRANT SELECT ON tabela TO user;

        REVOKE CREATE TABLE FROM user;
