location.href // Mostra a URL

location.hash // Pega o que tiver depois do jogo da velha (#)

location.search // Mostra as query strings que são passados na URL do navegador

location.pathname // Caminho da página

location.assign("url") // Ele navega para uma URL e altera o histórico de navegação

location.reload() // Vai recarregar a página.

location.replace("url") // Similar ao assign, porém, substitui um histórico de navegação e não altera o length  do history.
