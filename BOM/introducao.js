innerHeight // Propriedade que refere-se a área útil do Browser, o zoom afeta essa propriedade.
innerWidth // Propriedade que refere-se a área útil do Browser, o zoom afeta essa propriedade

outerHeight // Propriedade que diferente do innerHeight que pega a área disponível, ele pega a largura total independente de ser ter algo tapando a tela ou não.
outerWidth // Propriedade que diferente do innerHeight que pega a área disponível, ele pega a largura total independente de ser ter algo tapando a tela ou não.

screenX // Propridade que refere-se a posição do browser na tela
screenY // Propridade que refere-se a posição do browser na tela